		public int[,] LoadMap() {

			Random rand = new Random();

			for (int y = 0; y < tileArray.GetLength(1); y++)
				for (int x = 0; x < tileArray.GetLength(0); x++) {
					if (rand.NextDouble() <= Settings.TileWallProbability)
                        if(rand.NextDouble() <= Settings.TileDestructableProbability)
                            tileArray[x, y] = 2;
                        else
						tileArray[x, y] = 1;
					else
						tileArray[x, y] = 0;
				}

            //Exceptions
            tileArray[0, 0] = 0;
            tileArray[tileArray.GetLength(0)-1, tileArray.GetLength(1)-1] = 0;

			return tileArray;
		}

